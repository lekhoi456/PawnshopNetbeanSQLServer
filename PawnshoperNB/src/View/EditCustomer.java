    /**
     * show info to field
     */
    public void showInfo() {
        try {
            customerModel = new CustomerModel();
        } catch (Exception ex) {
            Logger.getLogger(EditCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i = 0; i < customerModel.getList().size(); i++) {
            if (customerModel.getList().get(i).getCustomerId() == Home.customerSelected) {
                txtCustomerId.setText(Integer.toString(customerModel.getList().get(i).getCustomerId()));
                txtCustomerName.setText(customerModel.getList().get(i).getCustomerName());
                txtSocialId.setText(customerModel.getList().get(i).getSocialId());
                txtDateRange.setText(customerModel.getList().get(i).getDateRange());
                txtRegisteredDate.setText(customerModel.getList().get(i).getRegisteredPlace());
                txtPhoneNumber.setText(customerModel.getList().get(i).getPhoneNumber());
                txtAddress.setText(customerModel.getList().get(i).getAddress());
                if (customerModel.getList().get(i).getIsActive() != 0) {
                    chkDeactive.setSelected(true);
                }
                if (customerModel.getList().get(i).getCustomerImage().endsWith("0")) {
                    chkCustomerImage.setSelected(true);
                }
            }
        }
    }

    private void updateCustomer() {
        int isActive = 0;
        if (chkDeactive.isSelected()) {
            isActive = 1;
        }
        String haveImage = "1";
        if (chkCustomerImage.isSelected()) {
            haveImage = "0";
        }
        try {
            customerModel.updatePhoneAddress(Home.customerSelected, txtPhoneNumber.getText(), txtAddress.getText(), isActive, haveImage);
        } catch (SQLException ex) {
            Logger.getLogger(EditCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void callHome() {
        Home home = new Home();
        home.cardLayout.show(home.pnlRight, "pnlCardCustomer");
        home.setVisible(true);
        this.dispose();
    }

