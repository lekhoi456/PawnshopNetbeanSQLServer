    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        boolean isOk = true;
        String message = "";
        if (!Validation.phoneNumber(txtPhoneNumber.getText())) {
            isOk = false;
            message = message + "- Phone number must be 10+ digits.\n";
        }
        if (isOk == false) {
            JOptionPane.showMessageDialog(null, message, "Error list:", 0);
        } else {
            updateCustomer();
            JOptionPane.showMessageDialog(null, "Customer update successfully.", "Notification", 1);
            callHome();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

